// Напиши функцию getMiddleElement с дженериком, которая будет принимать список
// и возвращать средний элемент, если он существует,
// иначе возвращать null. Например, у списка из четырёх элементов — среднего элемента нет.
fun <T> getMiddleElement(lst: List<T>): T? {
    return if (lst.size % 2 == 1) {
        lst[lst.size / 2]
    } else null
}

