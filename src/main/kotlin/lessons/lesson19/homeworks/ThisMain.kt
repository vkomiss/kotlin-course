// Создай функцию transposition с двумя дженериками,
// которая принимает словарь (Map) с дженериками
// и возвращает словарь, в котором ключи и значения поменяны местами.
fun <K, V> transposition(map: Map<K, V>): Map<V, K> {
    val res = mutableMapOf<V, K>()
    for ((k, v) in map) {
        res[v] = k
    }
    return res
}

